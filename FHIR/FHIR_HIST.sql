TAX_CERT.FILE_EXPORT_TYPE   = TAX_DESC.FILE_EXPORT_TYPE AND 
TAX_CERT.FILE_RECORD_ID     = TAX_DESC.FILE_RECORD_ID AND 
TAX_CERT.TAXONOMY_CODE      = TAX_DESC.TAXONOMY_CODE  ;

