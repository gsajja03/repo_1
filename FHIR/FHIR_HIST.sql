-- Provider Qualification Details from 'Practitioner'

CREATE OR REPLACE TEMPORARY TABLE PROV_QUAL AS 
SELECT --qualification,
FILE_RECORD_ID, MAX(PROV_LICENSE) AS PROV_LICENSE, MAX(PROV_LICENSE_STATE)  AS PROV_LICENSE_STATE, 
MAX(PROV_LICENSE_STATUS) AS PROV_LICENSE_STATUS ,  MAX(Provider_Credential) AS PROV_CREDENTIAL,
MAX(Provider_Credential_DISPLAY) AS PROV_CREDENTIAL_DISPLAY,
MAX(Provider_Credential_extension_Code) AS PROV_CREDENTIAL_EXTENSION_CODE,
MAX(Provider_Credential_TYPE) PROV_CREDENTIAL_TYPE,  
MAX(issuer_display) AS issuer_display,
MAX(issuer_org_ref ) AS issuer_org_ref

FROM (SELECT DISTINCT qualification.value AS qualification,
A.FILE_RECORD_ID, qualification_identifier.value:value AS PROV_LICENSE

,CASE WHEN qual_extension_code_extension_extension.value:url = 'whereValid' THEN qual_extension_code_extension_extension_valueCodeableConcept_coding.value:code  END AS PROV_LICENSE_STATE
,CASE WHEN qual_extension_code_extension_extension.value:url = 'status' THEN qual_extension_code_extension_extension.value:valueCode END AS PROV_LICENSE_STATUS

,qualification_code_coding.value:code  AS Provider_Credential 
,qualification_code_coding.value:display AS Provider_Credential_DISPLAY
,SPLIT_PART(qualification_code_coding.value:system,'/',-1) AS Provider_Credential_TYPE
,qualification.value:extension[0].valueCode AS Provider_Credential_extension_Code
,qualification.value:issuer.display AS issuer_display
,qualification.value:issuer.reference AS issuer_org_ref

FROM   ${SNOWFLAKE_ENV}_RAW_INBOUND_DB.BCBSA.PDT_FHIR A  ,  MAX_PDT_REC B 

,lateral flatten(input=>FILE_CONTENT_RAW:qualification,outer=>true) qualification
,lateral flatten(input=>qualification.value:identifier,outer=>true) qualification_identifier
,lateral flatten(input=>qualification.value:code:coding,outer=>true) qualification_code_coding
,lateral flatten(input=>qualification.value:code:extension,outer=>true) qual_extension_code_extension
,lateral flatten(input=>qual_extension_code_extension.value:extension,outer=>true) qual_extension_code_extension_extension
,lateral flatten(input=>qual_extension_code_extension_extension.value:valueCodeableConcept:coding,outer=>true) qual_extension_code_extension_extension_valueCodeableConcept_coding
,lateral flatten(input=>qualification.value:extension,outer=>true) qual_extension_extension
WHERE  A.FILE_RECORD_ID = B.FILE_RECORD_ID AND A.FILE_LAST_MODIFIED = B.FILE_LAST_MODIFIED AND SPLIT_PART(FILE_NAME, '/', 3) = '${BCBSA_FILE_EXPORT_TYPE}'   AND B.FILE_RESOURCE_TYPE = 'Practitioner' AND  A.FILE_RESOURCE_TYPE = 'Practitioner'  
AND qualification IS NOT NULL 

) GROUP BY qualification,FILE_RECORD_ID;


CREATE OR REPLACE TEMPORARY TABLE PROV_QUAL_1 AS 
SELECT 
    FILE_RECORD_ID,
    ARRAY_AGG(  
        OBJECT_CONSTRUCT(
            'PROV_LICENSE', PROV_LICENSE,
            'PROV_LICENSE_STATE', PROV_LICENSE_STATE,
            'PROV_LICENSE_STATUS', PROV_LICENSE_STATUS
        )
    ) WITHIN GROUP (ORDER BY PROV_LICENSE_STATE ASC) AS PROV_LICENSES
FROM (
    SELECT DISTINCT
        FILE_RECORD_ID,
        UPPER(REPLACE(TRIM(PROV_LICENSE), ' ', '')) AS PROV_LICENSE,
        UPPER(TRIM(PROV_LICENSE_STATE)) AS PROV_LICENSE_STATE,
        UPPER(TRIM(PROV_LICENSE_STATUS)) AS PROV_LICENSE_STATUS
    FROM PROV_QUAL
    WHERE PROV_LICENSE IS NOT NULL
      AND TRIM(PROV_LICENSE) <> ''
)
GROUP BY FILE_RECORD_ID 
ORDER BY FILE_RECORD_ID;




